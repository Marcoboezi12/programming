def add(a, b):
    return a + b

def subtract(a, b):
    return a - b

def multiply(a, b):
    return a * b

number1 = float(input("First number: "))
number2 = float(input("Second number: "))

print("Add:", add(number1, number2))
print("Subtract:", subtract(number1, number2))
print("Multiply:", multiply(number1, number2))